// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
#include <stack>
#include <vector>
using namespace std;
class Solution {
public:
    vector<int> inorderTraversal(TreeNode* root) {
        std::stack<TreeNode*> visited;
        vector<int> result;
        auto curr = root;
        while (curr != nullptr || !visited.empty()) {
            while (curr != nullptr)
            {
                visited.push(curr);
                curr = curr->left;
            }
            auto elem = visited.top();
            visited.pop();
            result.push_back(elem->val);
            curr = elem->right;    
        }
        return result;
    }
};
